print (f"LA SUMA DE A + B ES : {suma}")
resta = a - b
print (f"LA RESTA DE A - B ES : {resta}")
multiplicacion = a * b
print (f"LA MULTIPLICACION DE A * B ES: {multiplicacion}")
division = a / b
print (f"LA DIVISION ENTRE A / B ES : {division}")
modulo = a % b
print (f"EL MODULO (RESTO DE DIVISION) ENTRE A % B ES : {modulo}")
potencia = a ** b 
print (f"LA POTENCIA DE A ELEVADO A B ES : {potencia}")
div_entera = a // b
print (f"LA DIVISION ENTERA ENTRE A // B ES: {div_entera}")

